        #region Importations
        public List<Parameter> ImportExcel(string path, int userId)
        {
            List<Parameter> lstParameter = [];

            var WorkBook = new XLWorkbook(path);
            var Rows = WorkBook.Worksheet(1).RangeUsed().RowsUsed();

            foreach (var row in Rows)
            {
                var rowNumber = row.RowNumber();

                if (rowNumber > 1)
                {
                    string Name = row.Cell(7).GetString();
                    string Value = row.Cell(8).GetString();
                    bool IsPrivate = Convert.ToBoolean(row.Cell(9).GetString());
                    

                    Parameter Parameter = new()
                    {
                        ParameterId = 0,
                        Active = true,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        UserCreationId = userId,
                        UserLastModificationId = userId,
                        Name = Name,
                        Value = Value,
                        IsPrivate = IsPrivate,
                        
                    };

                    lstParameter.Add(Parameter);
                }
            }

            return lstParameter;
        }
