        #region Importations
        public List<User> ImportExcel(string path, int userId)
        {
            List<User> lstUser = [];

            var WorkBook = new XLWorkbook(path);
            var Rows = WorkBook.Worksheet(1).RangeUsed().RowsUsed();

            foreach (var row in Rows)
            {
                var rowNumber = row.RowNumber();

                if (rowNumber > 1)
                {
                    string Email = row.Cell(7).GetString();
                    string Password = row.Cell(8).GetString();
                    int RoleId = Convert.ToInt32(row.Cell(9).GetString());
                    string ProfilePicture = row.Cell(10).GetString();
                    

                    User User = new()
                    {
                        UserId = 0,
                        Active = true,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        UserCreationId = userId,
                        UserLastModificationId = userId,
                        Email = Email,
                        Password = Password,
                        RoleId = RoleId,
                        ProfilePicture = ProfilePicture,
                        
                    };

                    lstUser.Add(User);
                }
            }

            return lstUser;
        }
