            DataColumn dtColumnRoleIdFordtRoleCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "RoleId"
            };
            dtRoleCopy.Columns.Add(dtColumnRoleIdFordtRoleCopy);

            DataColumn dtColumnActiveFordtRoleCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "Active"
            };
            dtRoleCopy.Columns.Add(dtColumnActiveFordtRoleCopy);

            DataColumn dtColumnDateTimeCreationFordtRoleCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "DateTimeCreation"
            };
            dtRoleCopy.Columns.Add(dtColumnDateTimeCreationFordtRoleCopy);

            DataColumn dtColumnDateTimeLastModificationFordtRoleCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "DateTimeLastModification"
            };
            dtRoleCopy.Columns.Add(dtColumnDateTimeLastModificationFordtRoleCopy);

            DataColumn dtColumnUserCreationIdFordtRoleCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "UserCreationId"
            };
            dtRoleCopy.Columns.Add(dtColumnUserCreationIdFordtRoleCopy);

            DataColumn dtColumnUserLastModificationIdFordtRoleCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "UserLastModificationId"
            };
            dtRoleCopy.Columns.Add(dtColumnUserLastModificationIdFordtRoleCopy);

            DataColumn dtColumnNameFordtRoleCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "Name"
            };
            dtRoleCopy.Columns.Add(dtColumnNameFordtRoleCopy);


            #endregion

            dtRoleOriginal = dtRole;

            foreach (DataRow DataRow in dtRoleOriginal.Rows)
            {
                dtRoleCopy.Rows.Add(DataRow.ItemArray);
            }

            var Sheet = Book.Worksheets.Add(dtRoleCopy);

            Sheet.ColumnsUsed().AdjustToContents();

            Book.SaveAs(path);
        }

        public void ExportToCSV(string path, List<Role> lstRole)
        {
            using var Writer = new StreamWriter(path);

            using var CsvWriter = new CsvWriter(Writer, CultureInfo.InvariantCulture);

